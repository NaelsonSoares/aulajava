package operadores;

public class Aula1 {
    public static void main(String[] args) {
        System.out.println(5 + 2 );
        System.out.println(5 - 2 );
        System.out.println(5 * 2 );
        System.out.println(5 / 2 );
        System.out.println(5.0 / 2 );
        System.out.println(5 % 2 );
    }
}
